def get_category(skill_name: str) -> Tuple[str, str]:
    """
    获取技能的分类
    
    Args:
        skill_name: 技能名称
        
    Returns:
        (大类, 子类)
    """
    # 1. 精确匹配
    if skill_name in CATEGORY_MAP:
        return CATEGORY_MAP[skill_name]
    
    # 2. 规则匹配（用于新技能自动分类）
    for keywords, big_cat, sub_cat in CATEGORY_RULES:
        if any(kw.lower() in skill_name.lower() for kw in keywords):
            return (big_cat, sub_cat)
    
    # 3. 默认分类
    return ("其他", "其他")


